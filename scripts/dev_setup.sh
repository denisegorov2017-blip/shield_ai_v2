#!/bin/bash

# Скрипт для автоматического исправления и проверки качества кода
# Проект: Shield AI v2.0

# --- Цвета для вывода ---
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m' # No Color

echo -e "${GREEN}--- Запуск скрипта автоисправления и проверки кода ---${NC}"

# --- Этап 1: Автоматическое форматирование (isort и black) ---
echo -e "\n${YELLOW}Этап 1: Форматирование кода (isort и black)...${NC}"
poetry run isort .
poetry run black .
echo -e "${GREEN}Форматирование завершено.${NC}"

# --- Этап 2: Автоматическое исправление ошибок линтера (ruff) ---
echo -e "\n${YELLOW}Этап 2: Автоматическое исправление ошибок линтера (ruff)...${NC}"
poetry run ruff --fix .
echo -e "${GREEN}Автоматическое исправление ruff завершено.${NC}"

# --- Этап 3: Интеграция с mypy (установка стабов) ---
echo -e "\n${YELLOW}Этап 3: Установка недостающих стабов для mypy...${NC}"
poetry run mypy --install-types
echo -e "${GREEN}Установка стабов завершена.${NC}"

# --- Этап 4: Финальная проверка (ruff и mypy) ---
echo -e "\n${YELLOW}Этап 4: Финальная проверка на оставшиеся ошибки...${NC}"

echo -e "\n${YELLOW}Проверка ruff:${NC}"
poetry run ruff .

echo -e "\n${YELLOW}Проверка mypy:${NC}"
poetry run mypy .

echo -e "\n${GREEN}--- Все проверки завершены. Ошибки, требующие ручного вмешательства, выведены выше. ---${NC}"